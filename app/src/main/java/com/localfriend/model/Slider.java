public class Slider {
    private String SliderID;
    private String SliderTitle;
    private String ImageURL;
    private String SliderType;
    private String ThumbImage;
    private String RedirectURL;
    private String SliderStart;
    private String SliderEnd;
    private String Creation;
    private String Modified;

    public String getSliderID() {
        return SliderID;
    }

    public void setSliderID(String sliderID) {
        SliderID = sliderID;
    }

    public String getSliderTitle() {
        return SliderTitle;
    }

    public void setSliderTitle(String sliderTitle) {
        SliderTitle = sliderTitle;
    }

    public String getImageURL() {
        return ImageURL;
    }

    public void setImageURL(String imageURL) {
        ImageURL = imageURL;
    }

    public String getSliderType() {
        return SliderType;
    }

    public void setSliderType(String sliderType) {
        SliderType = sliderType;
    }

    public String getThumbImage() {
        return ThumbImage;
    }

    public void setThumbImage(String thumbImage) {
        ThumbImage = thumbImage;
    }

    public String getRedirectURL() {
        return RedirectURL;
    }

    public void setRedirectURL(String redirectURL) {
        RedirectURL = redirectURL;
    }

    public String getSliderStart() {
        return SliderStart;
    }

    public void setSliderStart(String sliderStart) {
        SliderStart = sliderStart;
    }

    public String getSliderEnd() {
        return SliderEnd;
    }

    public void setSliderEnd(String sliderEnd) {
        SliderEnd = sliderEnd;
    }

    public String getCreation() {
        return Creation;
    }

    public void setCreation(String creation) {
        Creation = creation;
    }

    public String getModified() {
        return Modified;
    }

    public void setModified(String modified) {
        Modified = modified;
    }
}
